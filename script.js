// State variables matching LocalStorage persistence
let gameState = {
    currentDay: 1,
    streak: 0,
    gold: 0,
    weaponArsenal: [],
    completedDaysList: [],
    stageCompleted: false // false: listen, true: transform, locked: done for day
};

// Initialize Application Engine
function initGymApp() {
    loadProgress();
    renderHistoryGrid();
    updateDashboardUI();
    setupEventListeners();
    showCurrentStage();
}

function loadProgress() {
    const saved = localStorage.getItem('english_gym_save_v1');
    if (saved) {
        try {
            gameState = JSON.parse(saved);
        } catch(e) {
            console.error("Failed to load save state", e);
        }
    }
    // Safeguard arrays
    if(!gameState.weaponArsenal) gameState.weaponArsenal = [];
    if(!gameState.completedDaysList) gameState.completedDaysList = [];
}

function saveProgress() {
    localStorage.setItem('english_gym_save_v1', JSON.stringify(gameState));
}

function updateDashboardUI() {
    document.getElementById('streak-count-display').innerText = gameState.streak;
    document.getElementById('progress-day-label').innerText = `第 ${gameState.currentDay} 天 / 第 90 天`;
    
    let pct = Math.round((gameState.currentDay / 90) * 100);
    document.getElementById('progress-percentage-label').innerText = pct + '%';
    document.getElementById('progress-bar-fill').style.width = pct + '%';
    
    document.getElementById('stat-gold').innerText = gameState.gold;
    document.getElementById('stat-weapon-count').innerText = gameState.weaponArsenal.length;
    document.getElementById('stat-mastered').innerText = gameState.completedDaysList.length;
}

function showCurrentStage() {
    // Hide all sub-stages first
    document.getElementById('substage-listen').style.display = 'none';
    document.getElementById('substage-transform').style.display = 'none';
    document.getElementById('substage-lockout').style.display = 'none';
    
    // Check if current day is already completed
    if (gameState.completedDaysList.includes(gameState.currentDay)) {
        document.getElementById('substage-lockout').style.display = 'block';
        return;
    }
    
    const dayData = ENGLISH_GYM_DATA[gameState.currentDay - 1] || ENGLISH_GYM_DATA[0];
    
    if (!gameState.stageCompleted) {
        document.getElementById('substage-listen').style.display = 'block';
    } else {
        document.getElementById('substage-transform').style.display = 'block';
        document.getElementById('transform-chinglish-text').innerText = dayData.chinglish;
        document.getElementById('transform-authentic-text').innerText = dayData.english;
        document.getElementById('transform-explanation-text').innerText = dayData.explanation;
        
        // Reset dynamic visibility buttons
        document.getElementById('transform-authentic-box').style.display = 'none';
        document.getElementById('btn-transform-reveal').style.display = 'block';
        document.getElementById('btn-transform-collect').style.display = 'none';
    }
}

function speakPhrase(text) {
    if ('speechSynthesis' in window) {
        window.speechSynthesis.cancel();
        const utterance = new SpeechSynthesisUtterance(text);
        utterance.lang = 'en-US';
        utterance.rate = 0.9; // clear pacing
        window.speechSynthesis.speak(utterance);
    } else {
        alert("🔊 你的浏览器暂不支持语音合成，请尝试使用 Chrome 浏览器！");
    }
}

function triggerModal(title, body) {
    document.getElementById('modal-title-text').innerText = title;
    document.getElementById('modal-body-text').innerText = body;
    document.getElementById('app-feedback-modal').classList.add('active');
}

// Flashcard arsenal logic review index
let activeReviewIndex = 0;

function renderArsenalCard() {
    const zone = document.getElementById('arsenal-active-zone');
    const empty = document.getElementById('arsenal-empty-state');
    
    if (gameState.weaponArsenal.length === 0) {
        zone.style.display = 'none';
        empty.style.display = 'block';
        return;
    }
    
    zone.style.display = 'block';
    empty.style.display = 'none';
    
    if(activeReviewIndex >= gameState.weaponArsenal.length) {
        activeReviewIndex = 0; // wrap around loop
    }
    
    const item = gameState.weaponArsenal[activeReviewIndex];
    document.getElementById('review-front-text').innerText = item.chinese || "地道短句挑战";
    document.getElementById('review-back-english').innerText = item.english;
    document.getElementById('review-back-explanation').innerText = item.explanation;
    
    // reset flip animation state
    document.getElementById('review-card-inner').classList.remove('flipped');
}

function renderHistoryGrid() {
    const grid = document.getElementById('history-grid-box');
    grid.innerHTML = '';
    for(let d=1; d<=90; d++) {
        const dot = document.createElement('div');
        dot.style.height = '24px';
        dot.style.borderRadius = '6px';
        dot.style.display = 'flex';
        dot.style.alignItems = 'center';
        dot.style.justifyContent = 'center';
        dot.style.fontSize = '10px';
        dot.innerText = d;
        
        if (gameState.completedDaysList.includes(d)) {
            dot.style.backgroundColor = 'var(--accent-success)';
            dot.style.color = '#fff';
        } else if (d === gameState.currentDay) {
            dot.style.backgroundColor = 'var(--accent-color)';
            dot.style.color = '#fff';
            dot.style.fontWeight = 'bold';
        } else {
            dot.style.backgroundColor = '#2e2e35';
            dot.style.color = 'var(--text-secondary)';
        }
        grid.appendChild(dot);
    }
}

function setupEventListeners() {
    // Stage 1 Trigger
    document.getElementById('listen-play-trigger').addEventListener('click', () => {
        const dayData = ENGLISH_GYM_DATA[gameState.currentDay - 1] || ENGLISH_GYM_DATA[0];
        speakPhrase(dayData.english);
    });
    
    document.getElementById('btn-listen-done').addEventListener('click', () => {
        gameState.stageCompleted = true;
        saveProgress();
        showCurrentStage();
    });
    
    // Stage 2 Trigger
    document.getElementById('btn-transform-reveal').addEventListener('click', () => {
        document.getElementById('transform-authentic-box').style.display = 'block';
        document.getElementById('btn-transform-reveal').style.display = 'none';
        document.getElementById('btn-transform-collect').style.display = 'block';
        
        // speak the golden sentence automatically
        const dayData = ENGLISH_GYM_DATA[gameState.currentDay - 1] || ENGLISH_GYM_DATA[0];
        speakPhrase(dayData.english);
    });
    
    document.getElementById('btn-transform-collect').addEventListener('click', () => {
        const dayData = ENGLISH_GYM_DATA[gameState.currentDay - 1] || ENGLISH_GYM_DATA[0];
        
        // push to weapon database if not exist
        if (!gameState.weaponArsenal.some(x => x.english === dayData.english)) {
            gameState.weaponArsenal.push(dayData);
        }
        
        // lock day rewards
        gameState.completedDaysList.push(gameState.currentDay);
        gameState.gold += 50;
        gameState.streak += 1;
        
        triggerModal("🎉 今日闭环达成！", "金币 +50! 地道短句已成功收纳进你的【武器库卡片】中。明天继续保持！");
        
        saveProgress();
        updateDashboardUI();
        renderHistoryGrid();
        showCurrentStage();
    });
    
    // Flashcard Flip Engine
    document.getElementById('review-card-inner').addEventListener('click', function() {
        this.classList.toggle('flipped');
    });
    
    document.getElementById('btn-swipe-fail').addEventListener('click', () => {
        activeReviewIndex++;
        renderArsenalCard();
    });
    
    document.getElementById('btn-swipe-success').addEventListener('click', () => {
        gameState.gold += 5; // tiny bonus
        updateDashboardUI();
        saveProgress();
        activeReviewIndex++;
        renderArsenalCard();
    });
    
    // Debug Cheat Next Day
    document.getElementById('btn-cheat-next-day').addEventListener('click', () => {
        if(gameState.currentDay < 90) {
            gameState.currentDay += 1;
            gameState.stageCompleted = false;
            saveProgress();
            updateDashboardUI();
            renderHistoryGrid();
            showCurrentStage();
        } else {
            alert("你已经全满通关90天挑战！");
        }
    });
    
    // Navigation routing setup
    const screens = ['screen-daily', 'screen-arsenal', 'screen-history'];
    const tabs = ['nav-trigger-daily', 'nav-trigger-arsenal', 'nav-trigger-history'];
    
    tabs.forEach((tabId, idx) => {
        document.getElementById(tabId).addEventListener('click', () => {
            tabs.forEach(t => document.getElementById(t).classList.remove('active'));
            screens.forEach(s => document.getElementById(s).classList.remove('active'));
            
            document.getElementById(tabId).classList.add('active');
            document.getElementById(screens[idx]).classList.add('active');
            
            if(screens[idx] === 'screen-arsenal') {
                renderArsenalCard();
            }
        });
    });
    
    document.getElementById('modal-close-trigger').addEventListener('click', () => {
        document.getElementById('app-feedback-modal').classList.remove('active');
    });
}

window.onload = initGymApp;

